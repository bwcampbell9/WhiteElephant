package com.example.white_elephant;

public class Item {

    private String name;
    private String desc;
    private int approxVal;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getApproxVal() {
        return approxVal;
    }

    public void setApproxVal(int approxVal) {
        this.approxVal = approxVal;
    }
}
